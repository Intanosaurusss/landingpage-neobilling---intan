<script>
    document.addEventListener('DOMContentLoaded', function () {
    const dropdowns = document.querySelectorAll('.faq-dropdown');

    dropdowns.forEach(dropdown => {
        const toggleButton = dropdown.querySelector('.faq-dropdown-toggle');
        const dropdownMenu = dropdown.querySelector('.faq-dropdown-menu');
        const icon = toggleButton.querySelector('.rotate-icon'); // hanya icon kedua yang diberi class rotate-icon

        toggleButton.addEventListener('click', function () {
            // Tutup semua dropdown lainnya
            dropdowns.forEach(otherDropdown => {
                if (otherDropdown !== dropdown) {
                    const otherMenu = otherDropdown.querySelector('.faq-dropdown-menu');
                    const otherIcon = otherDropdown.querySelector('.rotate-icon');
                    otherMenu.classList.add('hidden');
                    otherIcon.style.transform = 'rotate(0deg)';
                }
            });

            // Toggle visibility dari dropdown ini
            dropdownMenu.classList.toggle('hidden');

            // Rotate icon untuk dropdown ini
            const isMenuVisible = !dropdownMenu.classList.contains('hidden');
            icon.style.transform = isMenuVisible ? 'rotate(180deg)' : 'rotate(0deg)';

            
        });
    });
});
</script>
